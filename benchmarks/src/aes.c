static void AES128_Encrypt(uint8_t *state, const uint8_t *roundKeys) {
    int round = 0;

    for (int i = 0; i < 16; i++)
        state[i] ^= roundKeys[i];

    for (round = 1; round <= 9; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);

        for (int i = 0; i < 16; i++)
            state[i] ^= roundKeys[round * 16 + i];
    }

    SubBytes(state);
    ShiftRows(state);

    for (int i = 0; i < 16; i++)
        state[i] ^= roundKeys[160 + i];
}

